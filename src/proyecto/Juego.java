    private void btnRendirseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRendirseActionPerformed
        // TODO add your handling code here:
        int confirmacion = JOptionPane.showConfirmDialog(null,"Â¿Desea retirarse del juego? ", "Cambio de Turno", JOptionPane.YES_NO_OPTION);
        
        if (confirmacion == JOptionPane.YES_OPTION){
            String perdedor = lblTurnos.getText().substring(10);
            String ganador;
             
            if (perdedor.equals(datos.UsuarioLogeado)){ 
                 ganador = datos.SegundoUsuario;

              if (TableroStratego.esHeroe) {
                JOptionPane.showMessageDialog(null, ganador + " Los villanos ha ganado ya que " + perdedor + 
                        " se retiro del juego" + " . Apretar OK para volver al Menu Principal"); 
                TableroStratego.contadorVictoriasVillanos++;
              }
              else {
                  JOptionPane.showMessageDialog(null, ganador + " Los heroes ha ganado ya que " + perdedor + 
                        " se retiro del juego" + " . Apretar OK para volver al Menu Principal"); 
                  TableroStratego.contadorVictoriasHeroes++;
              }
              String FECHA=String.valueOf(datos.getfECHA());
              String EstadoVictoria="Derrota";
              datos.agregarFecha(FECHA, datos.UsuarioLogeado, datos.SegundoUsuario, EstadoVictoria);
            }
            else {
                 ganador = datos.UsuarioLogeado;

                if (TableroStratego.esHeroe) {
                JOptionPane.showMessageDialog(null, ganador + " Los heroes ha ganado ya que " + perdedor + 
                        " se retiro del juego" + " . Apretar OK para volver al Menu Principal"); 
                TableroStratego.contadorVictoriasHeroes++;
              }
              else {
                  JOptionPane.showMessageDialog(null, ganador + " Los villanos ha ganado ya que " + perdedor + 
                        " se retiro del juego" + " . Apretar OK para volver al Menu Principal"); 
                  TableroStratego.contadorVictoriasVillanos++;
              }
                 String FECHA=String.valueOf(datos.getfECHA());
                 String EstadoVictoria="Victoria";
              datos.agregarFecha(FECHA, datos.UsuarioLogeado, datos.SegundoUsuario, EstadoVictoria);
            }

        Usuarios usuarioGanador = datos.buscarUsuario(ganador);
        if (usuarioGanador != null) {
            usuarioGanador.IncrementarPuntos(3);
        }       
            menu = new Menu(login, datos);
            menu.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnRendirseActionPerformed



