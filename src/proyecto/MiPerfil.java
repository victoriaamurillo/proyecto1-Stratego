     private void mostrarRanking() {
         StringBuilder ranking = new StringBuilder();
    Usuarios usuarioLogeado = datos.buscarUsuario(datos.UsuarioLogeado);

    if (usuarioLogeado != null) {
        ranking.append(usuarioLogeado.getPuntos()).append(" puntos\n");
    }
    lblPuntos.setText(ranking.toString());
     }

